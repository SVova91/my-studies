first = input('Введите число: ')
second = input('Введите число: ')
third = input('Введите число: ')
if first == third != second:
    print('Одинаковых чисел: 2')
elif first == second == third:
    print('Одинаковых чисел: 3')
elif first != second != third:
    print('Одинаковых чисел: 0')
else:
    print('Одинаковых чисел: 2')
